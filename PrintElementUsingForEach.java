class PrintElementUsingForEach 
{  
	public static void main(String args[]){  

	String name[] = new String[5];  
  
	name[0] = "Mayuri";  
	name[1] = "Anju";  
	name[2] = "Siddhu";  
	name[3] = "Manoj";
	name[4] = "Rakhi";

	for (String str : name){  
	System.out.println(str);  
	}  
    }  
}  